.MODEL SMALL
.DATA
A DW 1234H,5678H,6256H,7321H,8484H
KEY DW 8454H
N DW 5
MSG1 DB 10,13,'KEY FOUND$'
MSG2 DB 10,13,'KEY NOT FOUND$'
.CODE
MOV AX,@DATA
MOV DS,AX
MOV CX,0001H
MOV DX,N
MOV AX,KEY
LOOP1:CMP CX,DX
JA NOTFOUND
MOV BX,CX
ADD BX,DX
SHR BX,01
MOV SI,BX
DEC SI
ADD SI,SI
CMP AX,A[SI]
JE FOUND
JA ABOVE
MOV DX,BX
DEC DX
JMP LOOP1
ABOVE:MOV CX,BX
INC CX
JMP LOOP1
FOUND:MOV AH,09H
LEA DX,MSG1
INT 21H
JMP EXIT
NOTFOUND:MOV AH,09H
LEA DX,MSG2
INT 21H
EXIT:MOV AH,4CH
INT 21H
END
