PRINT MACRO MSG
  MOV AH,09H
  MOV DX,OFFSET MSG
  INT 21H
 ENDM
.MODEL SMALL
.DATA
  M1 DB 'ENTER FILENAME TO CREATE:',10,13,'$'
  M2 DB 'ENTER FILENAME TO DELETE:',10,13,'$'
  M3 DB 'FILE NOT FOUND',10,13,'$'
  M4 DB 10,13,'$'
  FILENAME DB 80 DUP(0)
  FILENAME1 DB 80 DUP(0)

.CODE
  MOV AX,@DATA
  MOV DS,AX
  PRINT M1
  MOV SI,OFFSET FILENAME1
 RDCHAR2:
  MOV AH,01H
  INT 21H
  CMP AL,0DH
  JE SKIP2
  MOV [SI],AL
  INC SI
  JMP RDCHAR2
 SKIP2:
  MOV CX,00
  MOV DX,OFFSET FILENAME1
  INT 21H
  PRINT M4
  PRINT M2
  MOV SI,OFFSET FILENAME
 RDCHAR1:
  MOV AH,01H
  INT 21H
  CMP AL,0DH
  JE SKIP1
  MOV [SI],AL
  INC SI
  JMP RDCHAR1
 SKIP1:
  MOV AH,41H
  MOV DX,OFFSET FILENAME
  INT 21H
  JNC SUCC
  PRINT M3
 SUCC:
  MOV AH,4CH
  INT 21H
 END
  
