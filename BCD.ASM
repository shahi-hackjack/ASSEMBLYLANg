  DISP MACRO MSG
  LEA DX,MSG
  MOV AH,09
  INT 21H
  ENDM

.MODEL SMALL
.DATA
        MSG1 DB 10,13,'ENTER THE ROW NO. (MIN:00,MAX:24) : $'
        MSG2 DB 10,13,'ENTER THE COL NO. (MIN:00,MAX:79) : $'
        MSG3 DB 10,13,'PRESS ANY KEY TO QUIT : $'
        ROW DB ?
        COL DB ?

.CODE
  MOV AX,@DATA
  MOV DS,AX
  DISP MSG1
  CALL READ
  MOV ROW,AL
  DISP MSG2
  CALL READ
  MOV COL,AL
  DISP MSG3
  MOV AH,02
  MOV DH,ROW
  MOV DL,COL
  INT 10H
  MOV BH,07
  MOV CX,0000
  MOV DX,184FH
  MOV AX,0600H
  INT 10H
  MOV AH,8
  INT 21H
  MOV AH,4CH
  INT 21H
  READ PROC
  MOV AH,01H
  INT 21H
  AND AL,0FH
  MOV BL,AL
  MOV AH,01H
  INT 21H
  AND AL,0FH
  MOV AH,BL
  AAD
  RET
  READ ENDP
  END
